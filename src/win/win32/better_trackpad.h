// Per-contact info
// Gotten from lParam
typedef struct {
    // USB HID Usage Page 0x01, Usage 0x30
    bt_i16 x;
    // USB HID Usage Page 0x01, Usage 0x31
    bt_i16 y;
    // USB HID Usage Page 0x0d, Usage 0x51
    bt_i16 id;
} bt_contact;

#define BT_CONTACT_LPARAM(lParam) (bt_contact){ \
    .x  = (bt_i16)(((bt_u64)(lParam) >>  0) & 0xffff), \
    .y  = (bt_i16)(((bt_u64)(lParam) >> 16) & 0xffff), \
    .id = (bt_i16)(((bt_u64)(lParam) >> 32) & 0xffff), \
}

// Per-sample info (shared between multiple contacts)
// Gotten from wParam
typedef struct {
    // USB HID Usage Page 0x0d, Usage 0x56
    // See bt_context.scan_time_exp for scientific exponent
    bt_u16 scan_time;
    // USB HID Usage Page 0x0d, Usage 0x54
    bt_u16 contact_count;
} bt_sample;

#define BT_SAMPLE_WPARAM(wParam) (bt_sample){ \
    .scan_time      = (bt_u16)(((bt_u64)(wParam) >>  0) & 0xffff), \
    .contact_count  = (bt_i16)(((bt_u64)(wParam) >> 16) & 0xffff), \
}

