/*

better_trackpad.h - Library to simplify and improve trackpad input on windows

To use this library, 
```
#define BT_IMPLEMENTATION 
```  
before including the header file in one of your c files.

To use this library:
    - Create a `bt_context`
    - Call `bt_init`, optionally check `has_trackpad` and/or `initialized`
    - Upon receiving a `WM_INPUT` message, call `bt_process_input`
    - Somewhere in your main application loop, call `bt_passive_update`
    - Call `bt_destroy` (if you are using malloc/free)

This library generates three types of messages:
    1) `BT_MSG_RAW` - Essentially the raw contact information, unprocessed
    2) `BT_MSG_CONTACT_*` - These will track contacts, giving enter, update,
        and exit messages separately
    3) `BT_MSG_GESTURE_*` - Higher level gestures

Each type of message can be selectively disabled during initialization.

You can also directly look at the members `num_contacts` and `contacts` in 
the `bt_context` directly for a polling style input system.

Note about gestures:
    - I would generally not recommend using `BT_MSG_GESTURE_PAN` messages
        because they do not incorporate system preferences about scrolling
        speed and/or direction
    - I have not found a good way to distinguish true `WM_MOUSEWHEEL` messages
        where the user is physically pressing control, and synthetic events 
        generated by the trackpad zoom gesture, but you can always look at the
        `gesture_state` member of the context to see if better_trackpad detects
        and zoom event and proceed accordingly in the mouse wheel message

Coordinate System:
    - Trackpads give a logical min and max in each axis, as well as a physical
        min and max
    - better_trackpad converts the physical measure into millimeters
    - See the `(x/y)_(min/max)_(logical/mm)` members in the context
    - +x is right, +y is toward the user

Memory Allocation:
    - better_trackpad supports two styles of memory allocation
        - malloc/free
        - arenas
    - To customize the malloc/free style, define `BT_MALLOC` and `BT_FREE`
    - For the arenas, define `BT_USE_ARENAS`.
        - Then, you are *required* to define `BT_ARENA`, `BT_ARENA_GET_POS`,
        `BT_ARENA_PUSH`, and `BT_ARENA_POP_TO`
    - Only `bt_init` allocates memory

Error Handling:
    - The most important thing is checking if the initialization failed,
        which can be done by checking the `initialized` member in the context
    - better_trackpad passively records errors in the `err_str` member 
        of the context
    - Error strings can be disabled with:
        `#define BT_ERROR_STRINGS 0`
    - Even if error strings are disabled, a non-NULL pointer in `err_str` 
        indicates that an error occurred

Bug Reporting - If this library does not work for you, plaese let me know on
GitHub, but include the following in your issue:
    1) `#define BT_DEBUG_INFO` This will print *a lot* of debug info about your
        device, and should help diagnose the issue
    2) The model of your laptop and/or trackpad

Note on Linking
    - better_trackpad requires `hid.lib` to work
    - By default, there is a pragma comment, but this can be disabled with
        `#define BT_NO_LIB_COMMENT`

For any other features, just refer to the comment above their definition. You 
only need to look at the stuff before the `#ifdef BT_IMPLEMENTATION`.

*/

