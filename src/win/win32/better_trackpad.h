// This message essentially passes on the unprocessed
// result of the associated WM_INPUT message
// wParam stores the associated `bt_sample` (use BT_SAMPLE_WPARAM)
// lParam stores the associated `bt_contact` (use BT_CONTACT_LPARAM)
#define BT_MSG_RAW (WM_APP + BT_MSG_ID_OFFSET + 0)

// Sent upon first detecting a contact
// lParam stores the associated `bt_contact` (use BT_CONTACT_LPARAM)
#define BT_MSG_CONTACT_ENTER (WM_APP + BT_MSG_ID_OFFSET + 1)

// Sent when a contact moves, filtering out repeated positions
// lParam stores the associated `bt_contact` (use BT_CONTACT_LPARAM)
#define BT_MSG_CONTACT_UPDATE (WM_APP + BT_MSG_ID_OFFSET + 2)

// Sent when a contact is detected as leaving
// `bt_passive_update` is required for this
// They are also sent from `bt_process_input` if that detects it
// lParam stores the associated `bt_contact` (use BT_CONTACT_LPARAM)
#define BT_MSG_CONTACT_EXIT (WM_APP + BT_MSG_ID_OFFSET + 3)

// Sent when a two-finger pan gesture is detected
// I would recommend against using this as it does not incorporate system
// settings about zoom speed and direction, and it does not work with more
// than two contacts (by design). I am doing the gesture recognition 
// regardless, so I figured I might as well expose it
// wParam stores an associated `bt_sample` (use BT_SAMPLE_WPARAM)
// lParam stores the associated `bt_gesture_pan` (use BT_GESTURE_PAN_LPARAM)
